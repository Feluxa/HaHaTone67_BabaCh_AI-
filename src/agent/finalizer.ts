import type { AgentState } from "./agentState";

/**
 * buildFallbackAnswer — формирует человекочитаемый ответ клиенту,
 * когда агент не завершил расследование штатно (isFinished === false).
 *
 * Вызывается из orchestrator как страховочная сетка перед финальной
 * Zod-валидацией. Все ветки гарантируют ≥ 20 символов — требование
 * EvaluatePayloadSchema (answer: z.string().min(20)).
 *
 * Логика приоритетов (от наиболее информативного к нейтральному):
 *   1. Evidence + currentHypothesis → упомянуть обе детали.
 *   2. Только evidence → упомянуть количество собранных фактов.
 *   3. Только currentHypothesis → включить текущую версию событий.
 *   4. Ничего нет → нейтральный ответ о незавершённом расследовании.
 */
export function buildFallbackAnswer(state: AgentState): string {
  const hasEvidence = state.evidence.length > 0;
  // Проверяем наличие непустой гипотезы (поле не обязательное и не min(1))
  const hasHypothesis =
    typeof state.currentHypothesis === "string" &&
    state.currentHypothesis.trim().length > 0;

  // ── Ветка 1: есть и доказательства, и гипотеза ──────────────
  if (hasEvidence && hasHypothesis) {
    const count = state.evidence.length;
    const noun = count === 1 ? "доказательство" : count < 5 ? "доказательства" : "доказательств";
    return (
      `Мы изучили ваше обращение и собрали ${count} ${noun}. ` +
      `Наша версия событий: ${state.currentHypothesis}. ` +
      `Для завершения расследования требуется дополнительная проверка.`
    );
  }

  // ── Ветка 2: только доказательства, гипотезы нет ────────────
  if (hasEvidence) {
    const count = state.evidence.length;
    const noun = count === 1 ? "факт" : count < 5 ? "факта" : "фактов";
    return (
      `Мы успели собрать ${count} ${noun} по вашему обращению, ` +
      `однако завершить расследование автоматически не получилось. ` +
      `Ваш запрос будет передан специалисту.`
    );
  }

  // ── Ветка 3: только гипотеза, доказательств нет ─────────────
  if (hasHypothesis) {
    return (
      `Расследование не удалось завершить в автоматическом режиме. ` +
      `Текущая версия событий: ${state.currentHypothesis}. ` +
      `Мы передадим ваш запрос специалисту для дальнейшего изучения.`
    );
  }

  // ── Ветка 4: нет ни доказательств, ни гипотезы ──────────────
  return (
    "Расследование вашего обращения не удалось завершить автоматически — " +
    "недостаточно данных для безопасного принятия решения. " +
    "Мы передадим ваш запрос специалисту службы поддержки."
  );
}
